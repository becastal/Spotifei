        private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
		int r = jTable1.getSelectedRow();
		if (r == -1) {
			JOptionPane.showMessageDialog(this, "Nenhuma musica selecionada.");
			return;
		}

		int m = jTable1.convertRowIndexToModel(r);
		DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
		int musicaId = (int) model.getValueAt(m, 0);
		int usuarioId = user.getId();

		try {
			if (controller.curtiu(usuarioId, musicaId)) {
				controller.descurtir(usuarioId, musicaId);
				model.setValueAt("Não", m, 4);
			} else {
				controller.curtir(usuarioId, musicaId);
				model.setValueAt("Sim", m, 4);
			}
		} catch (SQLException ex) {
			JOptionPane.showMessageDialog(
				this,
				"Erro ao (des)curtir música: " + ex.getMessage()
			);
		}			
        }//GEN-LAST:event_jButton2ActionPerformed

