	private void atualizaPesquisa() {
		String termo = jTextField1.getText();
		ArrayList<Musica> resultados;

		try {
			if (jRadioButton1.isSelected()) {
				resultados = controller.queryTitulo(termo);
			} else if (jRadioButton2.isSelected()) {
				resultados = controller.queryArtista(termo);
			} else {
				resultados = controller.queryGenero(termo);
			}
			if (!termo.isEmpty()) controller.inserirBusca(user.getId(), termo);

			DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
			resultados = resultados;
			model.setRowCount(0); 

			resultadoSelecao = resultados;
			for (Musica m : resultados) {
				model.addRow(new Object[]{
					m.getId(),
					m.getTitulo(),
					m.getArtista().getNome(),
					m.getGenero(),
					controller.curtiu(user.getId(), m.getId()) ? "Sim" : "Não"
				});
			}
		} catch (SQLException ex) {
			JOptionPane.showMessageDialog(
				this,
				"Erro ao buscar músicas: " + ex.getMessage()
			);
		}
	}

